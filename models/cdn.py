class CDN(nn.Module):

    def __init__(self, d_model=512, nhead=8, num_encoder_layers=6,
                 num_dec_layers_hopd=3, num_dec_layers_interaction=3, 
                 dim_feedforward=2048, dropout=0.1,
                 activation="relu", normalize_before=False,
                 return_intermediate_dec=False):
        super().__init__()
        dim_pose = 128
        encoder_layer = TransformerEncoderLayer(d_model, nhead, dim_feedforward,
                                                dropout, activation, normalize_before)
        encoder_norm = nn.LayerNorm(d_model) if normalize_before else None
        self.encoder = TransformerEncoder(encoder_layer, num_encoder_layers, encoder_norm)

        decoder_layer = TransformerDecoderLayer(d_model, nhead, dim_feedforward,
                                                dropout, activation, normalize_before)
        decoder_norm = nn.LayerNorm(d_model)
        self.decoder = TransformerDecoder(decoder_layer, num_dec_layers_hopd, decoder_norm,
                                          return_intermediate=return_intermediate_dec)


        interaction_decoder_layer = TransformerDecoderLayer(d_model, nhead, dim_feedforward,
                                                            dropout, activation, normalize_before)
        interaction_decoder_norm = nn.LayerNorm(d_model)
        self.interaction_decoder = TransformerDecoder(interaction_decoder_layer, num_dec_layers_interaction,
                                                      interaction_decoder_norm, return_intermediate=return_intermediate_dec)


        #newstart
        decoder_norm2 = nn.LayerNorm(d_model)
        decoder_layer2 = TransformerDecoderLayer(d_model, nhead, dim_feedforward,
                                                dropout, activation, normalize_before)
        self.decoder2 = TransformerDecoder(decoder_layer2, num_dec_layers_hopd, decoder_norm2,
                                          return_intermediate=return_intermediate_dec)

        #interaction_decoder_layer_p = TransformerDecoderLayer(d_model, nhead, dim_feedforward,
        #                                                    dropout, activation, normalize_before)
        #interaction_decoder_norm_p = nn.LayerNorm(d_model)
        #self.interaction_decoder_p = TransformerDecoder(interaction_decoder_layer_p, num_dec_layers_interaction,
        #                                              interaction_decoder_norm_p,
        #                                              return_intermediate=return_intermediate_dec)


        #end


        self._reset_parameters()

        self.d_model = d_model
        self.nhead = nhead
        self.poselinear = nn.Linear(d_model+dim_pose, d_model)
        self.pose_norm = nn.LayerNorm(dim_pose)
        #self.posemlp = nn.Sequential(nn.Linear(dim_pose+d_model, dim_feedforward),
        #                           nn.ReLU(),
        #                           nn.Linear(d_model*4, d_model))
        #self.pose_t = nn.Sequential(nn.LayerNorm(dim_pose),
        #                              nn.Dropout(0.1))




    def _reset_parameters(self):
        for p in self.parameters():
            if p.dim() > 1:
                nn.init.xavier_uniform_(p)

    def forward(self, src, pose, pose_attention, mask, query_embed, query_embed2, pos_embed):
        bs, c, h, w = src.shape
        src = src.flatten(2).permute(2, 0, 1)
        pose = pose.flatten(2).permute(2, 0, 1)
        pose_attention = pose_attention.flatten(2).permute(2, 0, 1)
        pos_embed = pos_embed.flatten(2).permute(2, 0, 1)
        query_embed = query_embed.unsqueeze(1).repeat(1, bs, 1)
        query_embed2 = query_embed2.unsqueeze(1).repeat(1, bs, 1)
        mask = mask.flatten(1)

        tgt = torch.zeros_like(query_embed)
        memory = self.encoder(src, src_key_padding_mask=mask, pos=pos_embed)


        hopd_out = self.decoder(tgt, memory, memory_key_padding_mask=mask,
                          pos=pos_embed, query_pos=query_embed)
        hopd_out = hopd_out.transpose(1, 2)

        # start

        #query_embed2 = query_embed
        tgt2 = torch.zeros_like(query_embed2)
        hopd_out2 = self.decoder2(tgt2, memory, memory_key_padding_mask=mask,
                                 pos=pos_embed, query_pos=query_embed2)
        hopd_out2 = hopd_out2.transpose(1, 2)


        # end



        interaction_query_embed = hopd_out2[-1] + hopd_out[-1]
        interaction_query_embed = interaction_query_embed.permute(1, 0, 2)


        interaction_query_embed2 = hopd_out2[-1]
        interaction_query_embed2 = interaction_query_embed2.permute(1, 0, 2)

        memory_p = memory + pose_attention * self.poselinear(torch.cat((memory, self.pose_norm(pose)), dim=2))
        interaction_tgt = torch.zeros_like(interaction_query_embed)
        interaction_decoder_out = self.interaction_decoder(interaction_tgt, memory_p, memory_key_padding_mask=mask,
                                  pos=pos_embed, query_pos=interaction_query_embed)
        interaction_decoder_out = interaction_decoder_out.transpose(1, 2)

        interaction_decoder_out2 = self.interaction_decoder(interaction_tgt, memory_p, memory_key_padding_mask=mask,
                                                           pos=pos_embed, query_pos=interaction_query_embed2)
        interaction_decoder_out2 = interaction_decoder_out2.transpose(1, 2)




        return hopd_out, hopd_out2, interaction_decoder_out, interaction_decoder_out2



class TransformerEncoder(nn.Module):

    def __init__(self, encoder_layer, num_layers, norm=None):
        super().__init__()
        self.layers = _get_clones(encoder_layer, num_layers)
        self.num_layers = num_layers
        self.norm = norm

    def forward(self, src,
                mask: Optional[Tensor] = None,
                src_key_padding_mask: Optional[Tensor] = None,
                pos: Optional[Tensor] = None):
        output = src

        for layer in self.layers:
            output = layer(output, src_mask=mask,
                           src_key_padding_mask=src_key_padding_mask, pos=pos)

        if self.norm is not None:
            output = self.norm(output)

        return output


class TransformerDecoder(nn.Module):

    def __init__(self, decoder_layer, num_layers, norm=None, return_intermediate=False):
        super().__init__()
        self.layers = _get_clones(decoder_layer, num_layers)
        self.num_layers = num_layers
        self.norm = norm
        self.return_intermediate = return_intermediate

    def forward(self, tgt, memory,
                tgt_mask: Optional[Tensor] = None,
                memory_mask: Optional[Tensor] = None,
                tgt_key_padding_mask: Optional[Tensor] = None,
                memory_key_padding_mask: Optional[Tensor] = None,
                pos: Optional[Tensor] = None,
                query_pos: Optional[Tensor] = None):
        output = tgt

        intermediate = []

        for layer in self.layers:
            output = layer(output, memory, tgt_mask=tgt_mask,
                           memory_mask=memory_mask,
                           tgt_key_padding_mask=tgt_key_padding_mask,
                           memory_key_padding_mask=memory_key_padding_mask,
                           pos=pos, query_pos=query_pos)
            if self.return_intermediate:
                intermediate.append(self.norm(output))

        if self.norm is not None:
            output = self.norm(output)
            if self.return_intermediate:
                intermediate.pop()
                intermediate.append(output)

        if self.return_intermediate:
            return torch.stack(intermediate)

        return output

